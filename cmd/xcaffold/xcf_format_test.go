func TestMarshalMultiKind_DeterministicOrder(t *testing.T) {
	config := &ast.XcaffoldConfig{
		Version: "1.0",
		Project: &ast.ProjectConfig{Name: "order-test"},
		ResourceScope: ast.ResourceScope{
			Agents: map[string]ast.AgentConfig{
				"zulu":  {Name: "zulu", Description: "Last"},
				"alpha": {Name: "alpha", Description: "First"},
			},
		},
	}

	out1, _ := MarshalMultiKind(config, "")
	out2, _ := MarshalMultiKind(config, "")
	assert.Equal(t, string(out1), string(out2), "output must be deterministic")

	// Alpha should appear before Zulu
	content := string(out1)
	alphaIdx := strings.Index(content, "name: alpha")
	zuluIdx := strings.Index(content, "name: zulu")
	assert.Less(t, alphaIdx, zuluIdx, "agents should be sorted alphabetically")
}

