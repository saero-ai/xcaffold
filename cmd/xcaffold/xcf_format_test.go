func TestMarshalMultiKind_BasicConfig(t *testing.T) {
	config := &ast.XcaffoldConfig{
		Version: "1.0",
		Project: &ast.ProjectConfig{Name: "test-project"},
		ResourceScope: ast.ResourceScope{
			Agents: map[string]ast.AgentConfig{
				"developer": {Name: "developer", Description: "Dev", Model: "sonnet"},
			},
			Skills: map[string]ast.SkillConfig{
				"tdd": {Name: "tdd", Description: "TDD"},
			},
			Rules: map[string]ast.RuleConfig{
				"security": {Name: "security", Description: "Security"},
			},
		},
	}

	out, err := MarshalMultiKind(config, "# Generated by test")
	require.NoError(t, err)
	content := string(out)

	// Must have header
	assert.Contains(t, content, "# Generated by test")

	// Must have kind: project document
	assert.Contains(t, content, "kind: project")
	assert.Contains(t, content, "name: test-project")

	// Must have resource kind documents
	assert.Contains(t, content, "kind: agent")
	assert.Contains(t, content, "name: developer")
	assert.Contains(t, content, "kind: skill")
	assert.Contains(t, content, "name: tdd")
	assert.Contains(t, content, "kind: rule")
	assert.Contains(t, content, "name: security")

	// Must have --- separators
	assert.True(t, strings.Count(content, "---") >= 3, "expected at least 3 document separators")

	// Must be parseable by the parser (round-trip)
	parsed, err := parser.Parse(strings.NewReader(content))
	require.NoError(t, err)
	assert.Equal(t, "test-project", parsed.Project.Name)
	assert.Contains(t, parsed.Agents, "developer")
	assert.Contains(t, parsed.Skills, "tdd")
	assert.Contains(t, parsed.Rules, "security")
}

